            function Countdown(time_distance){
                // 计算时间差
                var time_distance = time_distance;
                // 天
                var int_day = Math.floor(time_distance/86400000);
                // 时
                var int_hour = Math.floor(time_distance/3600000)
                time_distance -= int_hour * 3600000;
                // 分
                var int_minute = Math.floor(time_distance/60000)
                time_distance -= int_minute * 60000;
                // 秒
                var int_second = Math.floor(time_distance/1000)
                // 时分秒为单数时、前面加零
                if(int_day < 10){
                    int_day = "0" + int_day;
                }
                if(int_hour < 10){
                    int_hour = "0" + int_hour;
                }
                if(int_minute < 10){
                    int_minute = "0" + int_minute;
                }
                if(int_second < 10){
                    int_second = "0" + int_second;
                }
                var Distance = new Object();
                Distance.day = new String(int_day);
                Distance.hour = new String(int_hour);
                Distance.minute = new String(int_minute);
                Distance.second = new String(int_second);
                return Distance;
            }
